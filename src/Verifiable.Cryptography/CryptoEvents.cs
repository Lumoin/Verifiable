/// <summary>
/// Emitted when key material is generated. Carries algorithm and backend
/// metadata only — never raw key bytes.
/// </summary>
public sealed record KeyMaterialGeneratedEvent: CryptoEvent
{
    /// <summary>The cryptographic algorithm of the generated material.</summary>
    public CryptoAlgorithm Algorithm { get; init; }

    /// <summary>The purpose of the generated key (signing, verification, exchange).</summary>
    public Purpose Purpose { get; init; }

    /// <summary>
    /// How the bytes should be interpreted — direct material or a hardware handle.
    /// </summary>
    public MaterialSemantics MaterialSemantics { get; init; }

    /// <summary>
    /// The library or backend that performed the generation, e.g. "BouncyCastle",
    /// "MicrosoftCng", "NSec". Opaque string — the library cannot introspect
    /// third-party key generation internals.
