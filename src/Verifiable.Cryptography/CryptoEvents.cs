using System;
using System.Diagnostics;
using Verifiable.Cryptography.Context;

namespace Verifiable.Cryptography;

/// <summary>
/// Base record for all observable cryptographic events emitted by this library.
/// </summary>
/// <remarks>
/// <para>
/// Events are emitted by cryptographic operations (entropy generation, signing,
/// verification, key material creation) and consumed by application-provided
/// subscribers via <see cref="CryptoObservable.Events"/>. The library does not
/// own the bus, transport, or sink — those are application concerns.
/// </para>
/// <para>
/// <strong>Correlation via W3C TraceContext.</strong>
/// </para>
/// <para>
/// <see cref="TraceParent"/> carries the W3C <c>traceparent</c> header value
/// from <see cref="Activity.Current"/> at the time the event is constructed.
/// When an OTel trace is active this threads the event into the distributed
/// trace automatically — including through claims assessment, DB writes, and
/// remote calls. When no trace is active, <see cref="TraceParent"/> is
/// <see langword="null"/>, which is honest: no trace is active, so no trace
/// context is available.
/// </para>
/// <para>
/// Events can be forwarded as CloudEvents, anchored to a Merkle log,
/// or correlated with did:cel / did:keri entries using <see cref="EventId"/>
/// and <see cref="TraceParent"/> as external identifiers.
/// </para>
/// </remarks>
public abstract record CryptoEvent
{
    /// <summary>
    /// Unique identifier for this event. Can be referenced by external
    /// audit logs, Merkle trees, or did:cel entries.
    /// </summary>
    public Guid EventId { get; init; } = Guid.NewGuid();

    /// <summary>
    /// When the event occurred.
    /// </summary>
    public DateTimeOffset OccurredAt { get; init; }

    /// <summary>
    /// The component or entity that emitted this event. Typically a DID when
    /// available (e.g. <c>did:web:issuer.example.com</c>), or a library
    /// component name when no DID context is active.
    /// </summary>
    public string EmittedBy { get; init; } = string.Empty;

    /// <summary>
    /// W3C TraceContext <c>traceparent</c> header value captured from
    /// <see cref="Activity.Current"/> at event construction time.
    /// <see langword="null"/> when no OTel activity is active.
    /// </summary>
    public string? TraceParent { get; init; }

    /// <summary>
    /// W3C TraceContext <c>tracestate</c> header value.
    /// <see langword="null"/> when no OTel activity is active.
    /// </summary>
    public string? TraceState { get; init; }


    /// <summary>
    /// Captures the current W3C TraceContext from <see cref="Activity.Current"/>.
    /// Call this at event construction time inside the operation being observed.
    /// </summary>
    protected static (string? traceParent, string? traceState) CaptureTraceContext() =>
        (Activity.Current?.Id, Activity.Current?.TraceStateString);
}


/// <summary>
/// Emitted when cryptographically random bytes are drawn from an entropy source.
/// </summary>
/// <remarks>
/// Carries the source identity, byte count, purpose, and the health snapshot
/// of the source at the moment of generation. Together these form the
/// generation-time entropy provenance record that travels with the produced
/// <see cref="Nonce"/>, <see cref="Salt"/>, or other value via its
/// <see cref="Tag"/>.
/// </remarks>
public sealed record EntropyConsumedEvent: CryptoEvent
{
    /// <summary>The source that produced the bytes.</summary>
    public EntropySource Source { get; init; }

    /// <summary>How many bytes were drawn.</summary>
    public int ByteCount { get; init; }

    /// <summary>The purpose of the generated bytes (e.g. <see cref="Purpose.Nonce"/>).</summary>
    public Purpose Purpose { get; init; }

    /// <summary>
    /// Health of the source at the moment of generation. Carries the
    /// assessment that was most recently available — not necessarily
    /// performed at this exact instant.
    /// </summary>
    public EntropyHealthObservation HealthAtGeneration { get; init; } = EntropyHealthObservation.Unknown;


    /// <summary>
    /// Constructs an <see cref="EntropyConsumedEvent"/> capturing the current
    /// trace context automatically.
    /// </summary>
    public static EntropyConsumedEvent Create(
        EntropySource source,
        int byteCount,
        Purpose purpose,
        EntropyHealthObservation health,
        string emittedBy = "",
        TimeProvider? timeProvider = null)
    {
        (string? traceParent, string? traceState) = CaptureTraceContext();
        return new EntropyConsumedEvent
        {
            OccurredAt = (timeProvider ?? TimeProvider.System).GetUtcNow(),
            Source = source,
            ByteCount = byteCount,
            Purpose = purpose,
            HealthAtGeneration = health,
            EmittedBy = emittedBy,
            TraceParent = traceParent,
            TraceState = traceState
        };
    }
}


