/// <summary>
/// The outcome of a cryptographic signature verification operation.
/// </summary>
public enum VerificationOutcome
{
    /// <summary>Signature is cryptographically valid.</summary>
    Valid,

    /// <summary>
    /// Signature is not valid. In a regulated flow this is a security event.
    /// </summary>
    Invalid,

    /// <summary>
    /// Verification could not be completed due to an error (missing key,
    /// unsupported algorithm, etc.).
    /// </summary>
    Error
}